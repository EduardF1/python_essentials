import math

pi = -3.14
x, y, z = 15, 22, 33

# round to the nearest int
print(round(pi))

# round to the nearest greater int
print(math.ceil(pi))

# round to the nearest lower int
print(math.floor(pi))

# compute the distance from the given argument to 0
print(abs(pi))

# raise number to power
print(pow(pi,2))

# return the sqrt of the argument
print(math.sqrt(400))

# return the maximal value from the given set of values
print(max(x, y, z))

# return the minimal value from the given set of values
print(min(x, y, z))