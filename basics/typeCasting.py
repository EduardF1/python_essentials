# type casting = convert the data type of a value to another data type

x = 1       #int
y = 2.0     #float
z = '3'     #str

# simple prints
print(x)
print(y)
print(z)

# reassignment
y = int(y)
z = int(z)
x = float(x)

print(x)
print(y)
print(z*3)

# Use case for casting to str.
print('X is' + ' ' + str(x))
print('Y is' + ' ' + str(y))
print('Z is' + ' ' + str(z))